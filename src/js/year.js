const butEl = document.querySelector('.leap-year__but');
const inputEl = document.querySelector('.leap-year__input');
const resultEl = document.querySelector('.leap-year__result');

// butEl: Кнопка, яка запускає перевірку введеного року.
// inputEl: Поле введення, де користувач вводить рік для перевірки.
// resultEl: Елемент, куди виводиться результат перевірки (чи високосний рік чи ні).

butEl.addEventListener('click', () => {
    if (isNaN(inputEl.value) || inputEl.value === '') {
        // Перевірка, чи введене значення не є числом або порожнім
        resultEl.innerHTML = 'Ви не ввели число';
        resultEl.style.color = localStorage.getItem('theme') === 'true' ? '#fff' : '#000';
    }
    else {
        // Якщо введене значення є числом
        resultEl.innerHTML = inputEl.value % 4 === 0 ? 'Ви народилися у високосний рік!' : 'Ви народилися не у високосний рік!';
        resultEl.style.color = inputEl.value % 4 === 0 ? '#039900' : '#900';
        inputEl.value = ''; // Очищення поля введення після перевірки
    }
});

// addEventListener('click', ...): Цей метод встановлює обробник події для кліків на кнопці butEl.

// Перевірка на числове значення:

// isNaN(inputEl.value): Перевіряє, чи введене значення не є числом.
// inputEl.value === '': Перевіряє, чи поле введення порожнє.
// Обробка помилкових введень:

// Якщо умова isNaN(inputEl.value) || inputEl.value === '' виконується, в resultEl виводиться повідомлення "Ви не ввели число", і колір тексту встановлюється в залежності від налаштувань збереженого темного режиму (localStorage.getItem('theme') === 'true').
// Перевірка на високосний рік:

// inputEl.value % 4 === 0: Перевірка, чи введений рік кратний 4 (умова для високосного року за Григоріанським календарем).
// Виведення результату:

// Якщо рік високосний (inputEl.value % 4 === 0), в resultEl виводиться повідомлення "Ви народилися у високосний рік!" з зеленим кольором (#039900).
// Якщо рік не високосний, виводиться повідомлення "Ви народилися не у високосний рік!" з червоним кольором (#900).
// Очищення поля введення:

// inputEl.value = '';: Після кожної перевірки поле введення очищується, щоб користувач міг ввести новий рік для перевірки.
